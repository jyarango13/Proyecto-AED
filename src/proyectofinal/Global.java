    private void btnIngresar3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresar3ActionPerformed

        // TODO add your handling code here:
        int verificar = 0, tipo = 0, horasT = 0;
        if (!txtnombres.getText().isEmpty()) {
            verificar++;
        }
        if (rbaux.isSelected()) {
            tipo(1);
        } else if (rbobs.isSelected()) {
            tipo(2);
        } else if (rbmed.isSelected()) {
            tipo(3);
        } else if (rbcir.isSelected()) {
            tipo(4);
        }
        if (!txthoras.getText().isEmpty() && Integer.parseInt(txthoras.getText()) >= 10 && Integer.parseInt(txthoras.getText()) <= 60) {
            horasT++;
        }
        if (verificar == 0) {
            JOptionPane.showMessageDialog(null, "Faltan ingresar el nombre", "ERROR", JOptionPane.ERROR_MESSAGE);
        } else if (!rbaux.isSelected() && !rbobs.isSelected() && !rbmed.isSelected() && !rbcir.isSelected()) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar el tipo de trabajador.", "ERROR", JOptionPane.ERROR_MESSAGE);
        } else if (horasT == 0) {
            JOptionPane.showMessageDialog(null, "Error en horas trabajadas >=10 y <=60", "ERROR", JOptionPane.ERROR_MESSAGE);
            txthoras.requestFocus();
        } else {
            JOptionPane.showMessageDialog(null, "Datos validados", "CONFORME", JOptionPane.INFORMATION_MESSAGE);
            //btnIngresar.setEnabled(true);
            String nombres = txtnombres.getText();
            int horas = Integer.parseInt(txthoras.getText());
            cola3.offer(nombres, tipoGlobal, horas);
            btg.clearSelection();
            verTabla3(dtmEjercicio3);

            // txtATrabajadores.setText(cola3.print());
            // btnIngresar.setEnabled(false);
            txtnombres.setText("");
            txthoras.setText("");
            txtnombres.requestFocus();
            txtgastoTrabajadores.setText(String.valueOf(cola3.gastoTrabajadores()));
        }
    }//GEN-LAST:event_btnIngresar3ActionPerformed

